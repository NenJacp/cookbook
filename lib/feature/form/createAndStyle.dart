import 'package:flutter/material.dart';

void main() => runApp(const CreateAndStyleApp());

class CreateAndStyleApp extends StatelessWidget {
  const CreateAndStyleApp({super.key});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'Create and Style',
      home: Scaffold(
        appBar: AppBar(
          title: const Text('Create and Style'),
          leading: IconButton(
            icon: const Icon(Icons.arrow_back),
